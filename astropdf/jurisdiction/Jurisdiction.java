package astropdf.jurisdiction;

/** 
 Abbreviations for a given country's states or provinces.
 
 This implementation uses the standard Canada Post codes for provinces and territories.
 For other countries, you will need to change this class to the appropriate values. 
*/
public enum Jurisdiction {
  AB, BC, MB, NB, NL, NS, NU, ON, PE, QC, SK, YT;
}
